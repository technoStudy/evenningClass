package day14;

public class Task3 {
    // using for loop, print all even numbers from 0 to 20;

    public static void main(String[] args) {
        // 0 + 2 = 2
        // 2 + 2 = 4
        // 4 + 2 = 6
        for(int i = 0; i <= 20; i = i + 2) {
            System.out.println( i );
        }

    }

}
