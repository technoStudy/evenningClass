package day4;

public class Task1 {
    // Write a Java program to print an int, a double and a char on screen.

    // part 2
    // write meaningful output for variables
    // for example:
    // integer 40
    // double 9.76
    // character Z
    public static void main(String[] args) {

        int a = 5;
        double b = 9;
        char c = 'C';

        System.out.println( "integer " + a ); // integer 5
        System.out.println( "double " + b ); // double
        System.out.println( "character " + c );

    }

}
